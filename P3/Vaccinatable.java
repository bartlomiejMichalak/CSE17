import java.util.Date;
/**
 * Interface class for Vaccinatable.
 * getVaccineDate() returns the date of last vaccination. setVaccineDate() sets
 * the date of last vaccination.
 */
public interface Vaccinatable{
  
  public abstract Date getVaccineDate();
  
  public abstract void setVaccineDate(Date vaccineDate);
    
  }

                                      
                                      